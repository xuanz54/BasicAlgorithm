def numOfZero(n:int)->int:
    """
    计算n的阶乘中尾部零的个数。
    
    尾部零的个数由n!中10的因子个数决定，而10可以分解为2和5的乘积。
    由于在阶乘中，2的因子通常比5的因子多，因此我们只需要计算5的因子个数。
    
    :param n: 要计算的阶乘数
    :return: 尾部零的个数
    """
    count = 0  # 初始化计数器
    i = 5  # 从5开始，因为5是第一个包含因子5的数
    while n // i >= 1:  # 只要n能被i整除，就说明有5的因子
        count += n // i  # 累加5的因子数量
        i *= 5  # 扩大因子5的倍数，检查下一个5的倍数
    return count

# 测试用例
print(f"100!尾部的零的个数为{numOfZero(100)}")
print(f"4!尾部的零的个数为{numOfZero(4)}")